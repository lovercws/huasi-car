package org.huasi.car.system.entity;

import java.io.Serializable;
import java.util.Date;

public class SysFeedback implements Serializable {

	private static final long serialVersionUID = -8666397697396262712L;

	private int feedbackId;// 反馈主键
	private int userId;// 反馈的用户Id
	private String feedbackTitle;// 反馈标题
	private String feedbackContent;// 反馈内容
	private String feedbackImgUrl;// 反馈图片
	private int feedbackStatus;// 反馈状态 1 提出来 未处理 2 正在处理;3已处理 ；4 已删除
	private Date createTime;// 反馈时间

	public int getFeedbackId() {
		return feedbackId;
	}

	public void setFeedbackId(int feedbackId) {
		this.feedbackId = feedbackId;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getFeedbackTitle() {
		return feedbackTitle;
	}

	public void setFeedbackTitle(String feedbackTitle) {
		this.feedbackTitle = feedbackTitle;
	}

	public String getFeedbackContent() {
		return feedbackContent;
	}

	public void setFeedbackContent(String feedbackContent) {
		this.feedbackContent = feedbackContent;
	}

	public String getFeedbackImgUrl() {
		return feedbackImgUrl;
	}

	public void setFeedbackImgUrl(String feedbackImgUrl) {
		this.feedbackImgUrl = feedbackImgUrl;
	}

	public int getFeedbackStatus() {
		return feedbackStatus;
	}

	public void setFeedbackStatus(int feedbackStatus) {
		this.feedbackStatus = feedbackStatus;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

}

package org.huasi.car.log.entity;

import java.io.Serializable;

import org.huasi.car.common.core.entity.BaseLog;

/**
 * @desc 商铺、服务搜索记录日志表
 * @author ganliang
 * @version 2016年8月10日 下午4:01:27
 */
public class MerSearchLog extends BaseLog implements Serializable {

	private static final long serialVersionUID = -2275902155874449669L;

	private Integer userId;// 用户id
	private String searchContent;// 搜索内容

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getSearchContent() {
		return searchContent;
	}

	public void setSearchContent(String searchContent) {
		this.searchContent = searchContent;
	}

}
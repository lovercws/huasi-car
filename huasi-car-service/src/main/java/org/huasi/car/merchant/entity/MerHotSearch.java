package org.huasi.car.merchant.entity;


import java.io.Serializable;

/**
 * @desc 热门商铺引用表 
 * @author ganliang
 * @version 2016年8月10日 下午3:55:23
 */
public class MerHotSearch implements Serializable{

	private static final long serialVersionUID = 4905323533700193108L;

	private Integer hotsearchId;// 搜索主键
    private String hotsearchKey;// 搜索关键词
    private Integer hotsearchCount;// 搜索次数

    public Integer getHotsearchId() {
        return hotsearchId;
    }

    public void setHotsearchId(Integer hotsearchId) {
        this.hotsearchId = hotsearchId;
    }

    public String getHotsearchKey() {
        return hotsearchKey;
    }

    public void setHotsearchKey(String hotsearchKey) {
        this.hotsearchKey = hotsearchKey == null ? null : hotsearchKey.trim();
    }

    public Integer getHotsearchCount() {
        return hotsearchCount;
    }

    public void setHotsearchCount(Integer hotsearchCount) {
        this.hotsearchCount = hotsearchCount;
    }
}
package org.huasi.car.order.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * @desc 订单支付记录
 * @author ganliang
 * @version 2016年8月25日 下午3:27:10
 */
public class OrderPaymentRecord implements Serializable {

	private static final long serialVersionUID = 3148347552254151620L;

	private int payId;// 支付记录id
	private int orderId;// 订单id

	private String outTradeNo;// 订单编号（订单流水号）
	private String outTradeType;// 交易业务类型 ：消费、充值等
	private String payStatus;// 支付记录状态

	private int payerUserId;// 付款人用户id
	private int payerPayAmount;// 付款方支付金额

	private int receiverUserId;// 收款方用户id
	private int receiverPayAmount;// 收款方金额

	private String orderIp;// 下单ip
	private String orderReferenceUrl;// 从哪个页面链接过来的(可用于防诈骗)
	private int orderAmount;// 订单金额
	private String orderPayType;// 支付方式 app支付；web支付；wap支付
	private String orderPayWay;// 支付方式(ALIPAY 支付宝；WXPAY微信；)
	private String payDesc;// 支付记录描述

	private Date orderTime;// 下单时间
	private Date paySuccessTime;// 订单支付成功改时间
	private Date completeTime;// 订单完成时间

	private String body;// 商品内容和描述
	private String attach;// 附件内容
	private String transactionId;// 支付方订单id
	private String prepareId;// 预支付id
	private String deviceInfo;// 微信支付分配的终端设备号，与下单一致

	public int getPayId() {
		return payId;
	}

	public void setPayId(int payId) {
		this.payId = payId;
	}

	public int getOrderId() {
		return orderId;
	}

	public void setOrderId(int orderId) {
		this.orderId = orderId;
	}

	public String getOutTradeNo() {
		return outTradeNo;
	}

	public void setOutTradeNo(String outTradeNo) {
		this.outTradeNo = outTradeNo;
	}

	public String getOutTradeType() {
		return outTradeType;
	}

	public void setOutTradeType(String outTradeType) {
		this.outTradeType = outTradeType;
	}

	public String getPayStatus() {
		return payStatus;
	}

	public void setPayStatus(String payStatus) {
		this.payStatus = payStatus;
	}

	public int getPayerUserId() {
		return payerUserId;
	}

	public void setPayerUserId(int payerUserId) {
		this.payerUserId = payerUserId;
	}

	public int getPayerPayAmount() {
		return payerPayAmount;
	}

	public void setPayerPayAmount(int payerPayAmount) {
		this.payerPayAmount = payerPayAmount;
	}

	public int getReceiverUserId() {
		return receiverUserId;
	}

	public void setReceiverUserId(int receiverUserId) {
		this.receiverUserId = receiverUserId;
	}

	public int getReceiverPayAmount() {
		return receiverPayAmount;
	}

	public void setReceiverPayAmount(int receiverPayAmount) {
		this.receiverPayAmount = receiverPayAmount;
	}

	public String getOrderIp() {
		return orderIp;
	}

	public void setOrderIp(String orderIp) {
		this.orderIp = orderIp;
	}

	public String getOrderReferenceUrl() {
		return orderReferenceUrl;
	}

	public void setOrderReferenceUrl(String orderReferenceUrl) {
		this.orderReferenceUrl = orderReferenceUrl;
	}

	public int getOrderAmount() {
		return orderAmount;
	}

	public void setOrderAmount(int orderAmount) {
		this.orderAmount = orderAmount;
	}

	public String getOrderPayType() {
		return orderPayType;
	}

	public void setOrderPayType(String orderPayType) {
		this.orderPayType = orderPayType;
	}

	public String getOrderPayWay() {
		return orderPayWay;
	}

	public void setOrderPayWay(String orderPayWay) {
		this.orderPayWay = orderPayWay;
	}

	public String getPayDesc() {
		return payDesc;
	}

	public void setPayDesc(String payDesc) {
		this.payDesc = payDesc;
	}

	public Date getOrderTime() {
		return orderTime;
	}

	public void setOrderTime(Date orderTime) {
		this.orderTime = orderTime;
	}

	public Date getPaySuccessTime() {
		return paySuccessTime;
	}

	public void setPaySuccessTime(Date paySuccessTime) {
		this.paySuccessTime = paySuccessTime;
	}

	public Date getCompleteTime() {
		return completeTime;
	}

	public void setCompleteTime(Date completeTime) {
		this.completeTime = completeTime;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}

	public String getAttach() {
		return attach;
	}

	public void setAttach(String attach) {
		this.attach = attach;
	}

	public String getTransactionId() {
		return transactionId;
	}

	public void setTransactionId(String transactionId) {
		this.transactionId = transactionId;
	}

	public String getPrepareId() {
		return prepareId;
	}

	public void setPrepareId(String prepareId) {
		this.prepareId = prepareId;
	}

	public String getDeviceInfo() {
		return deviceInfo;
	}

	public void setDeviceInfo(String deviceInfo) {
		this.deviceInfo = deviceInfo;
	}

}

package org.huasi.car.info.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * 动态的分类
 */
public class ActiveClassify implements Serializable {

	private static final long serialVersionUID = 1163424450909918247L;

	private int activeClassifyId;// 动态分类的id
	private String activeClassifyName;// 动态分类名称
	private String activeClassifyDesc;// 动态分类描述
	private String activeClassifyImg;// 动态分类的图片
	private int activeClassifyOrder;// 动态分类的排序
	private int activeClassifyStatus;// 动态分类的状态 0 禁用；1正常；2删除
	private int activeCount;// 动态分类下的动态数量
	private Date activeClassifyCreateTime;// 动态分类创建时间

	public int getActiveClassifyId() {
		return activeClassifyId;
	}

	public void setActiveClassifyId(int activeClassifyId) {
		this.activeClassifyId = activeClassifyId;
	}

	public String getActiveClassifyName() {
		return activeClassifyName;
	}

	public void setActiveClassifyName(String activeClassifyName) {
		this.activeClassifyName = activeClassifyName;
	}

	public String getActiveClassifyDesc() {
		return activeClassifyDesc;
	}

	public void setActiveClassifyDesc(String activeClassifyDesc) {
		this.activeClassifyDesc = activeClassifyDesc;
	}

	public String getActiveClassifyImg() {
		return activeClassifyImg;
	}

	public void setActiveClassifyImg(String activeClassifyImg) {
		this.activeClassifyImg = activeClassifyImg;
	}

	public int getActiveClassifyOrder() {
		return activeClassifyOrder;
	}

	public void setActiveClassifyOrder(int activeClassifyOrder) {
		this.activeClassifyOrder = activeClassifyOrder;
	}

	public int getActiveClassifyStatus() {
		return activeClassifyStatus;
	}

	public void setActiveClassifyStatus(int activeClassifyStatus) {
		this.activeClassifyStatus = activeClassifyStatus;
	}

	public int getActiveCount() {
		return activeCount;
	}

	public void setActiveCount(int activeCount) {
		this.activeCount = activeCount;
	}

	public Date getActiveClassifyCreateTime() {
		return activeClassifyCreateTime;
	}

	public void setActiveClassifyCreateTime(Date activeClassifyCreateTime) {
		this.activeClassifyCreateTime = activeClassifyCreateTime;
	}

}

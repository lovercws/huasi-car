package org.hausi.car.jpush.exception;

/**
 * 推送消息出现异常
 * @author ganliang
 */
public class JPushMessageException extends Exception {

	private static final long serialVersionUID = -398457421021748850L;

	public JPushMessageException() {
		super();
	}

	public JPushMessageException(String e, Throwable arg1) {
		super(e, arg1);
	}

	public JPushMessageException(String e) {
		super();
	}

	public JPushMessageException(Throwable e) {
		super();
	}

}

package org.hausi.car.jpush.enums;

/**
 * 极光推送手机平台类型
 * @author ganliang
 */
public enum JPushPlatform {
	/**
	 * 所有的设备
	 */
	ALL("all"),

	/**
	 * android
	 */
	Android("android"),

	/**
	 * ios
	 */
	IOS("ios"),

	/**
	 * winphone
	 */
	WinPhone("winphone");

	private final String value;

	private JPushPlatform(final String value) {
		this.value = value;
	}

	public String value() {
		return this.value;
	}
}

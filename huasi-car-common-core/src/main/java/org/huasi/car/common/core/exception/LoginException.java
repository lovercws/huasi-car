package org.huasi.car.common.core.exception;

/**
 * @desc 用户登录注册的时候出现异常
 * @author ganliang
 * @version 2016年8月12日 上午10:06:38
 */
public class LoginException extends Exception {

	private static final long serialVersionUID = -1872106187773828826L;

	public LoginException() {
		super();
	}

	public LoginException(String message, Throwable cause) {
		super(message, cause);
	}

	public LoginException(String message) {
		super(message);
	}

	public LoginException(Throwable cause) {
		super(cause);
	}

}

package org.huasi.car.common.core.enums;

/**
 * @desc 商铺订单状态
 * @author ganliang
 * @version 2016年8月22日 上午10:26:05
 */
public enum OrderStatusEnum {
	//////////////////////////////// 用户操作
	/**
	 * 用户取消订单
	 */
	CANCEL,

	/**
	 * 订单生成 未付款；待付款
	 */
	NOT_PAY,

	/**
	 * 订单付款成功 等待商户同意
	 */
	PAYMENT_SUCCESS,

	/**
	 * 用户点击订单完成(未评价)
	 */
	FINISH,

	/**
	 * 用户订单评论完毕
	 */
	COMMENT,

	/**
	 * 用户订单退款中
	 */
	REFUNDING,

	/**
	 * 订单退款成功
	 */
	REFUND_SUCCESS,

	/**
	 * 订单退款失败
	 */
	REFUND_FAIL,

	//////////////////////////////// 商户操作
	/**
	 * 商户取消订单
	 */
	MERCHANT_CANCEL,

	/**
	 * 商户同意下单
	 */
	AGREE,

	/**
	 * 用户到达时候,商户点击用户已到达
	 */
	ARRIVE;

}

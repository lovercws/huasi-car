package org.huasi.car.common.core.enums;

/**
 * @desc 反馈状态
 * @author ganliang
 * @version 2016年8月17日 上午9:24:58
 */
public enum FeedbackEnum {

	/**
	 * 刚提出反馈 1
	 */
	NORMAL(1),

	/**
	 * 正在处理
	 */
	DEALING(2),

	/**
	 * 处理完毕
	 */
	DEALED(3),

	/**
	 * 删除 3
	 */
	DELETE(4);

	/** 描述 */
	private int status;

	private FeedbackEnum(int status) {
		this.status = status;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int value() {
		return status;
	}
}
